class Note {
	x: number
	y: number
	width: number
	height: number
	text: string
	fontSize: number

	constructor(x: number, y: number, width: number, height: number, text: string, fontSize: number) {
		this.x = x
		this.y = y
		this.width = width
		this.height = height
		this.text = text
		this.fontSize = fontSize
	}

}

export default Note